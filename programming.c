int main()
{
    int a, b, c, d;

    scanf("%d %d %d %d", &a, &b, &c, &d);

    if(a <= b)
    {
        //a, c
        if(a <= c)
        {
            printf("%d", a);
        }
        else{
            printf("%d", c);
        }
    }
    else if(b <= c)
    {
        //b, c
        if(b <= c)
        {
            printf("%d", b);
        }
        else{
            printf("%d", c);
        }
    }
    else{
        //c, d
        if(c <= d)
        {
            printf("%d", c);
        }
        else{
            printf("%d", d);
        }
    }

    printf(" ");

    if(a >= c && a >= d)
    {
        printf("%d", a);
    }
    else if(b >= b && b >= d)
    {
        printf("%d", b);
    }
    else if(c >= a && c >= d)
    {
        printf("%d", c);
    }
    else
    {
        printf("%d", d);
    }
}